package com.bjpowernode.crm.workbench.domain;

/**
 * 显示漏斗图而设计的实体类
 */
public class FunnelVO {
	private String name;
	private int value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
